package com.demotivirus.Day_218;

public class FindMinAndMaxNumber {
    public static void main(String[] args) {
        int first = -45;
        int second = -15;

        System.out.println("Min: " + Math.min(first, second));
        System.out.println("Max: " + Math.max(first, second));

        double dl = 0.023844D;
        double d2 = 0.35468856D;

        System.out.println("Min: " + Double.min(dl, d2));
        System.out.println("Max: " + Double.max(dl, d2));
    }
}
